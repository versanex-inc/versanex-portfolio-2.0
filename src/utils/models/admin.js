import mongoose from "mongoose";

const adminModel = new mongoose.Schema({
  username: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
});

export const Admin = mongoose.models.admins || mongoose.model("admins", adminModel);